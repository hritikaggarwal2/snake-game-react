export const isUp = (key) => {
  return key === "w" || key === "ArrowUp";
};

export const isDown = (key) => {
  return key === "s" || key === "ArrowDown";
};

export const isLeft = (key) => {
  return key === "a" || key === "ArrowLeft";
};

export const isRight = (key) => {
  return key === "d" || key === "ArrowRight";
};
